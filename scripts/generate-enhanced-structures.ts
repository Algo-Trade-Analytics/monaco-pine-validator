function main() {
  const jsonlPath = path.join(__dirname, '../PineScriptContext/pinescript_reference.jsonl');
  const outputDir = path.join(__dirname, '../PineScriptContext/enhanced-structures');
  
  console.log('📖 Reading scraped documentation...');
  const content = fs.readFileSync(jsonlPath, 'utf-8');
  const lines = content.trim().split('\n');
  const entries: ScrapedEntry[] = lines.map(line => JSON.parse(line));
  
  console.log(`✅ Loaded ${entries.length} entries`);
  
  // Create output directory
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
    console.log(`📁 Created directory: ${outputDir}`);
  }
  
  // Generate files
  console.log('🔨 Generating TypeScript files...');
  
  const files = [
    { name: 'variables.ts', content: generateVariablesFile(entries) },
    { name: 'functions.ts', content: generateFunctionsFile(entries) },
    { name: 'constants.ts', content: generateConstantsFile(entries) },
    { name: 'keywords.ts', content: generateKeywordsFile(entries) },
    { name: 'operators.ts', content: generateOperatorsFile(entries) },
    { name: 'types.ts', content: generateTypesFile(entries) },
    { name: 'annotations.ts', content: generateAnnotationsFile(entries) },
    { name: 'index.ts', content: generateIndexFile() },
    { name: 'README.md', content: generateReadme() },
  ];
  
  files.forEach(({ name, content }) => {
    const filePath = path.join(outputDir, name);
    fs.writeFileSync(filePath, content);
    console.log(`  ✓ ${name} (${(content.length / 1024).toFixed(1)} KB)`);
  });
  
  // Generate stats
  const stats = {
    variables: entries.filter(e => e.type === 'variable').length,
    functions: entries.filter(e => e.type === 'function').length,
    constants: entries.filter(e => e.type === 'constant').length,
    keywords: entries.filter(e => e.type === 'keyword').length,
    operators: entries.filter(e => e.type === 'operator').length,
    types: entries.filter(e => e.type === 'type').length,
    annotations: entries.filter(e => e.type === 'annotation').length,
  };
  
  console.log('\n📊 GENERATION COMPLETE');
  console.log('─'.repeat(50));
  console.log(`Variables:   ${stats.variables} entries`);
  console.log(`Functions:   ${stats.functions} entries`);
  console.log(`Constants:   ${stats.constants} entries`);
  console.log(`Keywords:    ${stats.keywords} entries`);
  console.log(`Operators:   ${stats.operators} entries`);
  console.log(`Types:       ${stats.types} entries`);
  console.log(`Annotations: ${stats.annotations} entries`);
  console.log(`Total:       ${entries.length} entries`);
  console.log();
  console.log(`📂 Output directory: ${outputDir}`);
}

main();

