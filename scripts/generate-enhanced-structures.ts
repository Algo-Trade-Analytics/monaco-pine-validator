interface ScrapedEntry {
  id: string;
  name: string;
  description: string;
  type_info: string;
  category: string;
  type: 'variable' | 'function' | 'constant' | 'type' | 'annotation' | 'operator' | 'keyword';
  source_url: string;
  scraped_at: string;
  remarks?: string;
  see_also?: Array<{ name: string; reference: string }>;
  example?: string;
  returns?: string;
  syntax?: string;
  'syntax_&_overloads'?: string;
  parameters?: Array<{ text: string }>;
  fields?: string;
}

interface ParsedTypeInfo {
  qualifier: string | null;
  type: string;
}

function parseTypeInfo(typeInfo: string): ParsedTypeInfo {
  if (!typeInfo || typeInfo === 'undefined') {
    return { qualifier: null, type: 'void' };
  }
  
  const parts = typeInfo.trim().split(/\s+/);
  
  if (parts.length === 1) {
    return { qualifier: null, type: parts[0] };
  }
  
  const qualifier = parts[0];
  const type = parts.slice(1).join(' ');
  
  return { qualifier, type };
}

function sanitizeName(name: string): string {
  // Remove () from function names
  return name.replace(/\(\)$/, '');
}

function escapeString(str: string): string {
  return str
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\n/g, '\\n')
    .replace(/\r/g, '\\r')
    .replace(/\t/g, '\\t');
}

function generateVariablesFile(entries: ScrapedEntry[]): string {
  const variables = entries.filter(e => e.type === 'variable');
  
  let code = `// This file is automatically generated from scraped Pine Script v6 documentation.
// Do not edit this file directly.
// Generated at: ${new Date().toISOString()}

export interface PineVariableMetadata {
  readonly name: string;
  readonly qualifier: string | null;
  readonly type: string;
  readonly description: string;
  readonly remarks?: string;
  readonly example?: string;
  readonly seeAlso?: ReadonlyArray<{ name: string; reference: string }>;
}

`;

  // Build nested structure
  const varTree: any = {};
  
  variables.forEach(v => {
    const cleanName = sanitizeName(v.name);
    const parts = cleanName.split('.');
    const { qualifier, type } = parseTypeInfo(v.type_info);
    
    const metadata: any = {
      name: cleanName,
      qualifier,
      type,
      description: v.description,
    };
    
    if (v.remarks) metadata.remarks = v.remarks;
    if (v.example) metadata.example = v.example;
    if (v.see_also && v.see_also.length > 0) metadata.seeAlso = v.see_also;
    
    let current = varTree;
    for (let i = 0; i < parts.length; i++) {
      const part = parts[i];
      if (i === parts.length - 1) {
        current[part] = metadata;
      } else {
        if (!current[part] || typeof current[part] !== 'object' || current[part].name) {
          current[part] = {};
        }
        current = current[part];
      }
    }
  });
  
  code += 'export const variables: Record<string, PineVariableMetadata | any> = ';
  code += JSON.stringify(varTree, null, 2);
  code += ' as const;\n';
  
  return code;
}

